  --name fashionmnist \
  --image aicregistry:5000/mark:ddpm-ood \
  --backoff-limit 0 \
  --gpu ${num_gpus} \
  --cpu 12 \
  --large-shm \
  --host-ipc \
  --project mark \
  --run-as-user \
  --volume /nfs/home/mark/projects/ddpm-ood/ddpm-ood/:/project/ \
  --volume /nfs/home/mark/projects/ddpm-ood/data:/data/ \
  --volume /nfs/home/mark/projects/ddpm-ood/output/:/output/ \
